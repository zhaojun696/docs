# 标准误差
# 标准误差简单理解即是对平均数求标准差
# 比如一次实验会得到一个平均数，多次实验得到多个平均数，标准误差即是对这些平均数求标准差。

# 实际意义即是用来表示样本均值与总体均值的离散程度，标准误越小，样本均值和总体均值差距则越小，反之越大。
# 标准误差用于预测样本数据准确性 ，标准误越小，样本均值和总体均值差距越小，样本数据越能代表总体数据。

