# %%
# 标准差
# 反映的是一个数据集的离散程度，值越大，越离散，即个体间差异越大
# sqrt(((x1-x)^2 +(x2-x)^2 +......(xn-x)^2)/n)
#%%
import numpy as np

a=np.array(range(10))
# +100不会影响标准差
a.std(),((a*2)+100).std(),((a*2)).std()
#%%
# 均值/标准差的意义(用于不同单位的标准差【离散度】比较)
import numpy as np
arr=np.array([1,2,3,4,5,6,7])
arr10=arr*10
arr.std(),arr10.std(),arr.mean()/arr.std(),arr10.mean()/arr10.std()
#%%
# (值-均值)/标准差的意义(消除量纲影响,保留原有分布,标准差为1，均值为0)
import numpy as np
arr=np.array(range(100))
arr=(arr-arr.mean())/arr.std()
arr,arr.std(),arr.mean()
#%%
# n个标准正太分布相加，除以根号下n。的结果也为标准正太分布

a=np.random.normal(0,1,1000)
b=np.random.normal(0,1,1000)
c=(a+b)/np.sqrt(2)
a.std(),a.mean(),b.std(),b.mean(),c.std(),c.mean()


